package com.company.modulejava.lecture3;

public class Array4Demo {
    public static void main(String[] args) {
        int[] age = {12, 4, 5};

        // loop through the array
        // using for loop
        System.out.println("Using for-each Loop:");
        for (int a : age) {
            System.out.println(a);
        }
    }
}